# Código de comparação entre dois valores

primeiro_valor = input('Digite o primeiro valor: ')
segundo_valor = input('Digite o segundo valor: ')

if primeiro_valor == segundo_valor:
    print(f'O primeiro valor informado é igual ao segundo valor informado')
elif  primeiro_valor > segundo_valor:
    print(f'O valor {primeiro_valor} é maior do que {segundo_valor}')
else:
    print(f'O valor {segundo_valor} é maior do que {primeiro_valor}')

print('Fim  código')
